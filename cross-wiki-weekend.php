	<h1>Cross-Wiki Weekend</h1>

	Cross-Wiki Weekend is an annual event put on by NIWA for Nintendo's birthday.

	In late September, everyone is invited to constructively edit NIWA wikis that they have never (or infrequently) edited before.
	Whether it's adding some new content, uploading an image, or adding a reference, we want you to take your experience and love for Nintendo to a new wiki.
	<br><br>
	At the end of each Cross-Wiki Weekend, one random participant wins a Nintendo gift card!

